S = input()                  # 문자열 입력

count = 0                    # 뒤집는 횟수의 변수 선언

result = 0                   # 결과 변수 선언



for i in range(len(S)-1) :   # 길이 -1을 해야하는 이유는 i와 i+1로 비교하기에 인덱스가 맞지 않기 때문
    if S[i] != S[i+1] :      # 현재 항과 다음 항을 비교해서 서로 다르면
        count += 1           # 뒤집는 횟수 증가

result = (count+1)//2        # count +1인 이유는 서로 다른 개수가 홀수일 때  나머지가 발생하기에
                             # ex) 5개면 (5+1//2) = 3번으로 딱 나누어 떨어지게 뒤집어야하기 때문 

print(result)                # 결과 출력